//========================================================================
// 函数: void Delay_ms(unsigned int time)
// 描述: 软件延时函数
// 参数: time:延时时间
// 返回: 无
// 说明：单片机频率不同，延时效果不同
// 版本: V1.0, 2020.03.09
//========================================================================
void Delay_ms(unsigned int time)
{
	unsigned int x,y;
	for(x=time;x>0;x--)
		for(y=110;y>0;y--);
}